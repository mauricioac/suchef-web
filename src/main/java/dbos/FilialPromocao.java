package dbos;

public class FilialPromocao {
	private int filiais_id;
	private int promocoes_id;
	
	public int getFiliais_id() {
		return filiais_id;
	}
	
	public void setFiliais_id(int filiais_id) {
		this.filiais_id = filiais_id;
	}
	
	public int getPromocoes_id() {
		return promocoes_id;
	}
	
	public void setPromocoes_id(int promocoes_id) {
		this.promocoes_id = promocoes_id;
	}
}
